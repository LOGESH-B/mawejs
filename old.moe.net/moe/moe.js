$(document).ready(function()
{
    //-------------------------------------------------------------------------
    // Resize board (client area) height
    //-------------------------------------------------------------------------

    $(window).on("resize", function() {
        $("div#board").height(
            $(window).height()
            - $("div#menubar").outerHeight(true)
            - $("div#topbar").outerHeight(true)
            - $("div#statusbar").outerHeight(true)
            - 1
        ).find(".autoheight").trigger("input");
        ;
    }).trigger("resize");

    //-------------------------------------------------------------------------
    // All done
    //-------------------------------------------------------------------------

    statline("Loaded.");
});
