//-----------------------------------------------------------------------------
// Bridge
//-----------------------------------------------------------------------------

function dlgcall(cmd, ...args) {
  return window.ipc.callMain("host", [cmd, ...args]);
}

//-----------------------------------------------------------------------------
// System dialogs
//-----------------------------------------------------------------------------

export function appQuit(options) {
  return dlgcall("quit", options);
}
